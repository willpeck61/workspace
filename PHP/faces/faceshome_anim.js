 function init_twitterfeedclickhideembed()
 {
 	var div      = document.getElementById('twitterfeedclickhideembed');
        if(div)
        {
 	var tw = new OpacityTween(div,Tween.regularEaseInOut,100,0,.3);
 	div.startf = function ()
  	             {
                        div.style.overflow = 'visible';
  	                tw.onMotionFinished = undefined;
  	                tw.continueTo(100, .3);
  	                div.style.display = 'block';
                        
  	             }
        div.endf   = function ()
  	             {
                        tw.onMotionFinished = function(){ div.style.display = 'none'; tw.onMotionFinished = undefined; }
  	                tw.continueTo(0, .3);
  	             }
        }
 }
